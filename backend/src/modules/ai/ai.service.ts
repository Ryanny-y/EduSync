const sleep = (ms: number) => new Promise((resolve) => setTimeout(resolve, ms));

const retry = async <T>(
  fn: () => Promise<T>,
  retries = 2,
  delay = 1000,
): Promise<T> => {
  try {
    return await fn();
  } catch (error) {
    if (retries <= 0) throw error;

    console.log(`Retrying... attempts left: ${retries}`);
    await sleep(delay);

    return retry(fn, retries - 1, delay * 2); // exponential backoff
  }
};

